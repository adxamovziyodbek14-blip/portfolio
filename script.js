document.addEventListener('DOMContentLoaded', () => {
    // --- Theme Toggle Logic ---
    const themeToggleBtn = document.getElementById('theme-toggle');
    const htmlElement = document.documentElement;
    const themeIcon = themeToggleBtn.querySelector('i');

    function toggleTheme() {
        const currentTheme = htmlElement.getAttribute('data-theme');
        const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
        htmlElement.setAttribute('data-theme', newTheme);
        
        if (newTheme === 'dark') {
            themeIcon.classList.replace('fa-sun', 'fa-moon');
        } else {
            themeIcon.classList.replace('fa-moon', 'fa-sun');
        }
        localStorage.setItem('theme', newTheme);
    }

    const savedTheme = localStorage.getItem('theme');
    if (savedTheme) {
        htmlElement.setAttribute('data-theme', savedTheme);
        if (savedTheme === 'light') {
            themeIcon.classList.replace('fa-moon', 'fa-sun');
        }
    }

    themeToggleBtn.addEventListener('click', toggleTheme);


    // --- Language Switcher Logic ---
    const langBtn = document.getElementById('lang-btn');
    const langDropdown = document.querySelector('.lang-dropdown');
    
    langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langDropdown.classList.toggle('show');
    });

    const langLinks = document.querySelectorAll('.lang-dropdown a');
    langLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const selectedLang = e.target.getAttribute('data-lang');     
            setLanguage(selectedLang);
            langDropdown.classList.remove('show');
        });
    });

    document.addEventListener('click', (e) => {
        if(!e.target.closest('.lang-switcher')) {
            langDropdown.classList.remove('show');
        }
    });

    function setLanguage(lang) {
        localStorage.setItem('language', lang);
        
        // Update lang button text
        langBtn.innerHTML = `${lang.toUpperCase()} <i class="fa-solid fa-chevron-down"></i>`;
        
        // Update document lang
        document.documentElement.lang = lang;

        // Update all elements with data-i18n attribute
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerText = translations[lang][key];
            }
        });

        // Update title separately
        if(translations[lang] && translations[lang]['head_title']) {
            document.title = translations[lang]['head_title'];
        }

        // Update all placeholders
        const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
        placeholders.forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (translations[lang] && translations[lang][key]) {
                el.setAttribute('placeholder', translations[lang][key]);
            }
        });

        // Re-init typing effect with new translation
        if(translations[lang] && translations[lang]['hero_title']) {
            typeText(translations[lang]['hero_title']);
        }
    }

    // Load saved lang
    const savedLang = localStorage.getItem('language') || 'en';
    setLanguage(savedLang);


    // --- Typing Effect ---
    const typingElement = document.querySelector('.typing-text');
    let typingInterval;

    function typeText(text) {
        clearInterval(typingInterval);
        typingElement.textContent = '';
        let i = 0;
        
        typingInterval = setInterval(() => {
            if (i < text.length) {
                typingElement.textContent += text.charAt(i);
                i++;
            } else {
                clearInterval(typingInterval);
            }
        }, 100);
    }


    // --- Mobile Menu Toggle ---
    const mobileMenuBtn = document.querySelector('.mobile-menu-btn');
    const navLinksContainer = document.querySelector('.nav-links');
    const mobileMenuIcon = mobileMenuBtn.querySelector('i');

    mobileMenuBtn.addEventListener('click', () => {
        navLinksContainer.classList.toggle('active');
        if(navLinksContainer.classList.contains('active')) {
            mobileMenuIcon.classList.replace('fa-bars', 'fa-times');
        } else {
            mobileMenuIcon.classList.replace('fa-times', 'fa-bars');
        }
    });

    // Close mobile menu when link is clicked
    document.querySelectorAll('.nav-links a').forEach(link => {
        link.addEventListener('click', () => {
            navLinksContainer.classList.remove('active');
            mobileMenuIcon.classList.replace('fa-times', 'fa-bars');
        });
    });


    // --- Smooth Scrolling for Navbar Links ---
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if(targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                // Account for sticky header offset
                const headerOffset = 80;
                const elementPosition = targetElement.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.pageYOffset - headerOffset;
          
                window.scrollTo({
                     top: offsetPosition,
                     behavior: "smooth"
                });
            }
        });
    });
});
